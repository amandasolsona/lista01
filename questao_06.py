#Exercício do Cálculo de Área: Escreva um programa em Python que solicite ao usuário a largura e a altura
#de um retângulo. Calcule a área desse retângulo e exiba o resultado.

largura = float(input('Qual é sua largura ?'))
altura = float(input('Qual é a sua altura ?'))
area = largura*altura

print(f'A área desse retângulo é {area}')